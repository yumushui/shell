## Vim文本编辑器
-- 正常模式
    i I o O a A 进入插入模式
        i 在当前光标位置进入插入模式
	I 移动到当前光标所在的行首，进入到插入模式
	a 在当前光标位置之后进入插入模式
	A 移动到当前光标所在行尾，进入到插入模式
	o 在当前光标下一行进入插入模式
	O 在当前光标上一行进入插入模式
    v V ctrl+v 进入可视模式
    : 进入命令模式
    esc 回到正常模式
    h j k l 上下左右
    yy y$ 复制
    dd d$ 剪切
    p 粘贴
    u 撤销命令
    ctrl+r 重做命令
    x 删除单个字符
    r 替换单个字符
    G 定位指定的行
    ^$ 定位到行的开头和结尾

-- 命令模式
    w 写入文件
    w 文件名 另存文件
