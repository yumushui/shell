## 用户管理
-- 用户命令
    常用用户命令
        useradd 添加用户
	userdel 删除用户
	passwd 设置用户密码
	usermod 修改用户信息
	groupadd 添加用户组
	groupdel 删除用户组

    用户与用户组的概念
    用户的家目录 /home/用户名
    以管理员身份运行
        su 切换当前用户身份
	sudo 用 root 用户身份执行某条命令
	visudo 修改 sudo 命令的配置文件

-- 用户配置文件
    /etc/passwd 用户信息配置文件
    /etc/shadow 用户密码信息配置文件


## 权限管理
-- 权限的表示方法
    一般权限用三种字符表示
        r 
	w
	x

    文件权限和目录权限相同，但功能不同
        文件权限
	    r
	    w
	    x

	目录权限
	    rx 进入目录读取文件名
	    wx 修改目录内文件名
	    x 进入目录

	特殊权限
	    /etc/passwd 用户信息配置文件
	    t 增加了目录权限为 777 的目录的安全性，确保只有 root 用户和文件属主才可以操作自己的文件或目录


-- 权限相关命令
    chmod 修改权限
    chown 修改属主、属组
    chgrp 可以单独更改属组，不常用



