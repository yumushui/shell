-- Linux版本
    内核版本格式
        xx.yy.zz
	    xx 主版本
	    yy 次版本
	    zz 末版本
	小于 2.6 版本
	    次版本奇数为开发版
	    次版本偶数为稳定版
	大于 2.6 版本
            logterm 长期支撑版本
	    stable 稳定版本
	    mainline 主线开发版本

    常见发行版本
        Red Hat
	Ubuntu
	CentOS
	Debian
	Fedora

-- 重要概念
    root账号： 对应 windows 系统的管理员账号
    "/" 根目录： 对应 Windows 系统"我的电脑"


