## 帮助命令
-- 使用终端
    命令提示符
        $ 普通用户
	# root用户
    命令和路径补全： tab键
    命令的分类（使用type查看某个命令的所属分类）
        内部命令
	外部命令

-- 帮助命令和用法
    man
        man xxx : 获取 xxx 命令的帮助
	man l man :  查看 man 命令自身的帮助文档
    help
        help xxx : 获取内部命令的帮助
	xxx --help : 获取外部命令的帮助
        
    info
        比 man 更丰富的帮助信息，格式： info xxx

## 文件和目录管理
### 文件与目录查看命令 ls
    -l (小写L) 显示文件的详细信息
    -a 显示隐藏文件
    -r 逆序显示
    -t 按时间排序
    -R 递归显示

### 路径操作（分绝对路径和相对路径）
    cd 进入指定路径
    pwd 显示当前路径

### 建立和删除目录
    mkdir 新建目录
    rmdir 删除空目录

### 通配符
    * 匹配任意字符
    ? 匹配单个字符
    [xyz] 匹配 xyz 任意一个字符
    [a-z] 匹配字符范围
    [!xzy] 或 [^xyz] 匹配不在 xyz 中的任意字符

### 复制文件 cp
    -r 复制目录
    -p 保留用户权限时间
    -a 尽可能保留原始文件的属性，等同于 -dpR

### 删除文件 rm
    -r 递归删除
    -f 不提示

### 移动与重命名 mv
    移动文件 mv file1 dir1/
    重命名文件 mv file1 file2


